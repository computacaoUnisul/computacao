<h2>Qisso?</h2>
<p>Na verdade nem a gente sabe pra que isso serve.</p>
<p>Brincadeira, o objetivo dessa aplicação é servir como um acervo com informações úteis no cotidiano acadêmico.<br/>
Trabalhos, provas, todos eles separados por semestres, matérias, professores. Normalmente as informações viram uma bagunça, Google Drive, One Drive, Dropbox e tudo isso, sem contar que precisa ficar sempre pedindo para aquele seu amigo enrolador te enviar fotos ou o link.<br/>
</p>

<p>Esta aplicação tenta resolver estes problemas.</p>
